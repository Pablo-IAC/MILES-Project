      SUBROUTINE respic(xl,s)
c Subroutine to calculate the response of the I Cousin filter
c It calls the "hunt" and "polint" routines of the numerical recipes 
c WARNING: the provided wavelength should be double-precision
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      PARAMETER (nf=24)
      DIMENSION XV(nf),SV(nf)
c Cousin 1980
      data XV/7000.0d0,7100.0d0,7200.0d0,7300.0d0,7400.0d0,7500.0d0,
     &7600.0d0,7700.0d0,7800.0d0,7900.0d0,8000.0d0,8100.0d0,8200.0d0,
     &8300.0d0,8400.0d0,8500.0d0,8600.0d0,8700.0d0,8800.0d0,8900.0d0,
     &9000.0d0,9100.0d0,9200.0d0,9300.0d0/
      data SV/0.0d0,0.024070021881838072,0.23194748358862144,     
     &0.55579868708971547,0.78555798687089706,0.90809628008752730,          
     &0.96280087527352298,0.98687089715536103,0.99124726477024072,     
     &0.99562363238512031,1.0d0,1.0d0,0.99562363238512031,     
     &0.98468271334792123,0.95623632385120350,0.93654266958424504,     
     &0.89715536105032812,0.83588621444201316,0.71553610503282272,     
     &0.56892778993435444,0.40043763676148791,0.25164113785557984,     
     &0.052516411378555797,0.0d0/     
c      data SV/0.0d0,0.011d0,0.106d0,0.254d0,0.359d0,0.415d0,0.440d0
c     &,0.451d0,0.453d0,0.455d0,0.457d0,0.457d0,0.455d0,0.450d0,0.437d0
c     &,0.428d0,0.410d0,0.382d0,0.327d0,0.260d0,0.183d0,0.115d0,0.024d0
c     &,0.0d0/
c NO DESCOMENTAR: por cada llamada a respic se dividira SV por .457
c	do i=1,nf
c	  SV(i)=SV(i)/0.457d0
c	enddo
      xli=XV(1)
	xlf=XV(nf)
 	call hunt(XV,nf,xl,k1)
	if(xl.gt.xli.and.xl.lt.xlf)then
	   call polint(XV(k1-1),SV(k1-1),3,xl,s,dy)
	   if(s.gt.1.0d0) s=1.0d0
	   if(s.lt.0.0d0) s=0.0d0
	else
	   s=0.0d0
	endif
	RETURN
	END
