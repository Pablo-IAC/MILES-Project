      SUBROUTINE respusdss(xl,s)
c Subroutine to calculate the response of the u SDSS filter
c It calls the "hunt" and "polint" routines of the numerical recipes 
c WARNING: the provided wavelength should be double-precision
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      PARAMETER (nf=47)
      DIMENSION XV(nf),SV(nf)
c SDSS u filter
      data XV/2980.,3005.,3030.,3055.,3080.,3105.,3130.,3155.,3180.,
     &3205.,3230.,3255.,3280.,3305.,3330.,3355.,3380.,3405.,3430.,
     &3455.,3480.,3505.,3530.,3555.,3580.,3605.,3630.,3655.,3680.,
     &3705.,3730.,3755.,3780.,3805.,3830.,3855.,3880.,3905.,3930.,
     &3955.,3980.,4005.,4030.,4055.,4080.,4105.,4130./
      data SV/0.,9.21658997E-04,4.60829539E-03,1.19815674E-02,
     &2.39631347E-02,4.79262695E-02,8.57142881E-02,0.14838710,
     &0.22119817,0.29769585,0.37327191,0.44700465,0.51705068,
     &0.58433181,0.64516133,0.69677418,0.74009222,0.78156680,
     &0.81382489,0.84516132,0.88387102,0.92258072,0.94838709,
     &0.96221203,0.97050697,0.97972351,0.99078345,1.000,0.99907839,
     &0.98064518,0.94377881,0.89032263,0.81751150,0.72534561,
     &0.61935484,0.50599080,0.38064516,0.24700461,0.13364056,
     &6.91244230E-02,3.87096778E-02,2.02764980E-02,9.21659078E-03,
     &5.52995456E-03,3.68663599E-03,1.84331799E-03,0./
        xli=XV(1)
	xlf=XV(nf)
 	call hunt(XV,nf,xl,k1)
	if(xl.gt.xli.and.xl.lt.xlf)then
	   call polint(XV(k1-1),SV(k1-1),3,xl,s,dy)
	   if(s.gt.1.0d0) s=1.0d0
	   if(s.lt.0.0d0) s=0.0d0
	else
	   s=0.0d0
	endif
	RETURN
	END
